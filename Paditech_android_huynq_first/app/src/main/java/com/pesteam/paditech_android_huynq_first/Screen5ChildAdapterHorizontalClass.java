public class Screen5ChildAdapterHorizontalClass {

    private String img;
    private int status;
    private String name;

    public Screen5ChildAdapterHorizontalClass(String img, int status, String name) {
        this.img = img;
        this.status = status;
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
